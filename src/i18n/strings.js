export function tr(locale, key, params = null) {
  const dict = STRINGS[locale] ?? STRINGS.en;
  let value = key.startsWith('rooms.')
    ? dict.rooms[key.slice(6)] ?? STRINGS.en.rooms[key.slice(6)] ?? key
    : dict[key] ?? STRINGS.en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      value = value.replaceAll(`{${k}}`, v);
    }
  }
  return value;
}
